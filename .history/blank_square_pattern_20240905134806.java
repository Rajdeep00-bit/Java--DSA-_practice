import java.util.*;
public class blank_square_pattern {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter the column:");
        int n=sc.nextInt();
        System.out.println("Enter the row:");
        int m=sc.nextInt();
        for(int i=0;i<n;i++){
            for(int j=0;i<m;j++){
                if(i==0||j==0||i==(n-1)||j==(m-1));
            }
        }
    }
}
